    private static List<BigDecimal> getMulList(int icon) {
        List<BigDecimal> mulList = new ArrayList<>();
        for (int i = 1; i <= COLUMNS; i++) {
            int mul = getMul(icon, i);
            mulList.add(DecimalUtil.getBigDecimal2(mul * 1.0d / BASE_LINE));
        }
        return mulList;
    }

    public static int getRandomNormalIcon() {
        double ran = RandomUtil.nextDouble();
        double pro = 0d;
        for (int i = ICON_PRO.length - 1; i >= 0; i--) {
            pro += ICON_PRO[i];
            if (ran < pro) return ICONS_WITH_MULTIPLE[i];
        }
        return ICONS_WITH_MULTIPLE[ICONS_WITH_MULTIPLE.length - 1];
    }

    /**
     * 获取中奖线路的中奖倍数
     *
     * @param icon 中奖图标
     * @param line 中奖线
     * @return 中奖倍数
     */
    public static int getMul(int icon, int line) {
        for (int[] multiple : LotteryConfig.ICON_MULTIPLE) {
            if (multiple[0] == icon && multiple[1] == line) {
                return multiple[2];
            }
        }
        return 0;
    }

    public static int getScatterSize(double fixedValue) {
        double random = RandomUtil.nextDouble();
        for (int i = SCATTER_PRO.length - 1; i >= 0; i--) {
            if (random > SCATTER_PRO[i] * fixedValue) continue;

            return i + 1;
        }
        return 0;
    }

    public static int getDoubleMul(int type, double factor) {
        type = Math.max(type, 1);
        type -= 1;
        double ran = RandomUtil.nextDouble();
        double pro = 0d;
        for (int i = DOUBLE_MUL_PRO.length - 1; i >= 0; i--) {
            pro += DOUBLE_MUL_PRO[type][i];
            if (ran < pro * factor) return DOUBLE_MUL_FOR_TYPE[type][i];
        }
        return DOUBLE_MUL_FOR_TYPE[type][0];
    }
